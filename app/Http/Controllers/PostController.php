    /**
        * @OA\Post(
        * path="/api/store/post",
        * operationId="AddPost",
        * tags={"Post"},
        * summary="Add post",
        * description="Add new post here",
        *     @OA\RequestBody(
        *         @OA\JsonContent(),
        *         @OA\MediaType(
        *            mediaType="multipart/form-data",
        *            @OA\Schema(
        *               type="object",
        *               required={"slug","content"},
        *               @OA\Property(property="slug", type="text"),
        *               @OA\Property(property="content", type="text")
        *            ),
        *        ),
        *    ),
        *      @OA\Response(
        *          response=201,
        *          description="Register Successfully",
        *          @OA\JsonContent()
        *       ),
        *      @OA\Response(
        *          response=200,
        *          description="Register Successfully",
        *          @OA\JsonContent()
        *       ),
        *      @OA\Response(
        *          response=422,
        *          description="Unprocessable Entity",
        *          @OA\JsonContent()
        *       ),
        *      @OA\Response(response=400, description="Bad request"),
        *      @OA\Response(response=404, description="Resource Not Found"),
        * )
        */
    public function store(Request $request)
    {
        $rules=array(
            'slug'  =>"required|unique:posts",
            'content' =>"required"
        );

        $validator=Validator::make($request->all(),$rules);
        if($validator->fails()){ 
            Log::error('Register validation fails');
            return $validator->errors();}

        $mypost = new Post();
        $mypost->slug = $request->slug;
        $mypost->content = $request->content;
        $mypost->save();

        $response = ["message" =>'Nouveau Post',"content"=>$mypost];
        return response($response, Response::HTTP_OK);
    }

